public class EjemploSingleLineComments {

    public static void main(String[] args) {
        // Declaración de variables
        int num1 = 10;
        int num2 = 20;

        // Cálculo de la suma
        int suma = num1 + num2;

        // Impresión del resultado en la consola
        System.out.println("La suma de " + num1 + " y " + num2 + " es: " + suma);
    }
}
